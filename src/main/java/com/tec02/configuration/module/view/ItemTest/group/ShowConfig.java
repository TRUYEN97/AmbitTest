    public ShowConfig() {
        initComponents();
        this.showConfig = new ShowItemConfig();
        this.pnShowConfig.add(showConfig);
        this.timer = new Timer(500, (e) -> {
            String itemName = this.showConfig.getItemName();
            if(itemName == null){
                return;
            }
            if (!this.showConfig.isSaved()) {
                this.setTitle("*" + itemName);
            }else{
                this.setTitle(itemName);
            }
        });
        this.timer.start();
    }

