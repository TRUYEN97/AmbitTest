    public void askAndSave() throws HeadlessException {
        // TODO add your handling code here:
        if (!showConfig.isSaved() && showConfig.getConfig() != null && JOptionPane.showConfirmDialog(null,
                "Bạn có muốn lưu lại tất cả thay dổi không?",
                "Warning", JOptionPane.OK_CANCEL_OPTION) == JOptionPane.OK_OPTION) {
            showConfig.save();
        }
    }

