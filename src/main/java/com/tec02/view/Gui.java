    private void txtInputKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtInputKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (this.core.isMultiUICell()) {
                this.txtInputIndex.requestFocus();
            } else {
                this.core.setInput(this.txtInput.getText(),
                        this.txtInputIndex.getText());
                this.txtInput.setText(null);
            }
        }
        this.txtInputIndex.setText(null);
        showSfisText(null);
    }//GEN-LAST:event_txtInputKeyPressed

